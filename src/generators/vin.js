import {
  ALL_DIGITS,
  ALL_LETTERS_EXCEPT_IOQ_AND_ALL_DIGITS,
  ALL_LETTERS_EXCEPT_IOQUZ_AND_ALL_DIGITS_EXCEPT_0,
  generateRandomStringOfChars,
  randomChar,
  randomElement,
} from "../misc/randomUtils.js";
import { vinChecksum } from "../misc/checksumUtils.js";

const VIN_CHECKSUM_PLACEHOLER = "0";

/**
 * @returns {string}
 */
export function generateVin() {
  const wmi = randomElement(WMI_KNOWN_MANUFACTURERS);
  const vehicleAttributes = generateRandomStringOfChars(ALL_LETTERS_EXCEPT_IOQ_AND_ALL_DIGITS, 5);
  const modelYearCode = randomChar(ALL_LETTERS_EXCEPT_IOQUZ_AND_ALL_DIGITS_EXCEPT_0);
  const plantCode = randomChar(ALL_LETTERS_EXCEPT_IOQ_AND_ALL_DIGITS);
  const sequentialNumber = generateRandomStringOfChars(ALL_DIGITS, 6);

  const vinWithoutChecksum =
    wmi + vehicleAttributes + VIN_CHECKSUM_PLACEHOLER + modelYearCode + plantCode + sequentialNumber;
  const checksum = vinChecksum(vinWithoutChecksum);

  return wmi + vehicleAttributes + checksum + modelYearCode + plantCode + sequentialNumber;
}

